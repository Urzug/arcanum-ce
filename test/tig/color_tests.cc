#include "tig/color.h"

#include <gtest/gtest.h>

#include "tig/memory.h"

class TigColorTest : public testing::Test {
protected:
    void SetUp() override
    {
        ASSERT_EQ(tig_memory_init(NULL), TIG_OK);
    }

    void TearDown() override
    {
        ASSERT_TRUE(tig_memory_validate_memory_leaks());
        tig_memory_exit();
    }
};

class TigColorTestRGB565 : public TigColorTest {
protected:
    void SetUp() override
    {
        TigColorTest::SetUp();

        TigInitializeInfo init_info;
        init_info.bpp = 16;

        ASSERT_EQ(tig_color_init(&init_info), TIG_OK);
        ASSERT_EQ(tig_color_set_rgb_settings(0xF800, 0x7E0, 0x1F), TIG_OK);
    }

    void TearDown() override
    {
        tig_color_exit();

        TigColorTest::TearDown();
    }
};

TEST_F(TigColorTestRGB565, GetMasks)
{
    unsigned int red;
    unsigned int green;
    unsigned int blue;
    tig_color_get_masks(&red, &green, &blue);

    EXPECT_EQ(red, 0xF800);
    EXPECT_EQ(green, 0x7E0);
    EXPECT_EQ(blue, 0x1F);
}

TEST_F(TigColorTestRGB565, GetShifts)
{
    unsigned int red;
    unsigned int green;
    unsigned int blue;
    tig_color_get_shifts(&red, &green, &blue);

    EXPECT_EQ(red, 11);
    EXPECT_EQ(green, 5);
    EXPECT_EQ(blue, 0);
}

TEST_F(TigColorTestRGB565, Settings)
{
    EXPECT_EQ(tig_color_red_mask, 0xF800);
    EXPECT_EQ(tig_color_green_mask, 0x7E0);
    EXPECT_EQ(tig_color_blue_mask, 0x1F);

    EXPECT_EQ(tig_color_red_shift, 11);
    EXPECT_EQ(tig_color_green_shift, 5);
    EXPECT_EQ(tig_color_blue_shift, 0);

    EXPECT_EQ(tig_color_red_range, 31);
    EXPECT_EQ(tig_color_green_range, 63);
    EXPECT_EQ(tig_color_blue_range, 31);

    EXPECT_EQ(tig_color_red_range_bit_length, 5);
    EXPECT_EQ(tig_color_green_range_bit_length, 6);
    EXPECT_EQ(tig_color_blue_range_bit_length, 5);
}

